// POST INCREMENT
var foo = 1;
console.log("Post Increment with start value equals to "+foo);
console.log(foo++);
console.log(foo++);
console.log(foo++);
console.log("\n");
// PRE INCREMENT
var boo = 1;
console.log("Pre Increment with start value equals to "+boo);
console.log(++boo);
console.log(++boo);
console.log(++boo);
console.log("\n");
// POST DECREMENT
var coo = 10;
console.log("Post Decrement with start value equals to "+coo);
console.log(coo--);
console.log(coo--);
console.log(coo--);
console.log("\n");
// PRE DECREMENT
var doo = 10;
console.log("Pre Decrement with start value equals to "+doo);
console.log(--doo);
console.log(--doo);
console.log(--doo);
console.log("\n");
